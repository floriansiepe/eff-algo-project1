public class Robot {
    String name;
    int costM;
    int costF;
    int shoeSize;
    String fFood;
    String special;

    Robot (String name, int costM, int costF, int shoeSize, String fFood, String special){
        this.name = name;
        this.costM = costM;
        this.costF = costF;
        this.fFood = fFood;
        this.shoeSize = shoeSize;
        this.special = special;
    }

}
